import Foundation

class planetClass{
    
    private var _type: String!
    private var _name: String!
    private var _size: String!
    private var _distance: String!
    
    var type: String {
        return _type
    }
    var name: String {
        return _name
    }
    var size: String {
        return _size
    }
    var distance: String {
        return _distance
    }
    
}